#include <unistd.h>

void	ft_putnbr(int n)
{
	int		div;
	char	c;

	div = 1;
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	while ((n / div) >= 10)
		div *= 10;
	while (div > 0)
	{
		c = (n / div) % 10 + '0';
		write(1, &c, 1);
		div /= 10;
	}
}
