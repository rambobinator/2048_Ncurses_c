#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, size_t n)
{
	int		i;

	i = 0;
	while (n--)
	{
		if (s2[i] == '\0')
			s1[i] = '\0';
		else
			s1[i] = s2[i];
		i++;
	}
	return (s1);
}
